/*Програма знаходить значення у за формулою a*b*c/a+b+c - a-b-c/a*b*c*/
#include <stdio.h>
#include <math.h>

int main () {
    //Задаємо сталі величини
    int a=2, b=-2, c=22;
    float y;

    //Обчислюємо значення функції
    y= ((a*b*c)/(a+b+c)) - ((a-b-c)/(a*b*c));

    //Виводимо значення функції
    printf ("Значення функції: %.2f\n", y);

    return 0; //кінець програми

}