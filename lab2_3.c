/*Програма обчислює значення f за формулою  f=x^2+x*y-y^2/1+x^2+y^2*/
#include <stdio.h>
#include <math.h>

int main () {
    //Оголошуємо змінні
    int x,y;
    float f;

    //Введення змінних
    printf("Введіть значення x та y:");
    scanf("%d %d", &x, &y); 

    //Обчислюємо значення функції за формулою
    f=(pow(x,2)+x*y-pow(y,2))/(1+pow(x,2))+pow(y,2); 

    //Виводимо значення функції
    printf("Значення функції f: %f\n", f);

    return 0; //кінець програми

}